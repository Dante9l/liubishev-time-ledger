export function resolveLocale(languageCode?: string | null): Locale {
  const normalized = String(languageCode ?? "").trim().toLowerCase();
  if (normalized.startsWith("zh")) {
    return "zh";
  }

  return "en";
}

export function translate(
  locale: Locale,
  key: string,
  vars: Record<string, string | number | undefined> = {},
): string {
  const template = translations[locale][key] ?? translations.en[key] ?? key;
  return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) => String(vars[token] ?? ""));
}

export function createTranslator(locale: Locale): (key: string, vars?: Record<string, string | number | undefined>) => string {
  return (key, vars) => translate(locale, key, vars);
}
